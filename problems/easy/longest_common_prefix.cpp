class Solution {
public:
    string longestCommonPrefix(vector<string>& v) {
        string p = v[0];
        
        for (int i = 1; i < v.size(); i++) {
            while (v[i].find(p) != 0) {
                p.pop_back();
                if (p.empty()) return "";
            }
        }
        
        return p;
    }
};